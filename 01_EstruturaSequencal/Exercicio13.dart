// Construir um programa que leia três valores numéricos e calcular o valor da soma dos seus quadrados.

void main() {

  var valor1 = 2;
  var valor2 = 3;
  var valor3 = 4;

  var somaDosQuadrados = valor1 * valor1 + valor2 * valor2 + valor3 * valor3;

  print('A soma dos quadrados dos três valores é: $somaDosQuadrados');
}
