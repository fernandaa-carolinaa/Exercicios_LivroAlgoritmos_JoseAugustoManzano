// Ler quatro valores numéricos inteiros e apresentar os resultados das adições e multiplicações dos quatro números ente si.

void main() {
  // Definindo os valores numéricos inteiros
  var valor1 = 2;
  var valor2 = 3;
  var valor3 = 4;
  var valor4 = 5;

  // Realizando as operações de adição e multiplicação
  var soma = valor1 + valor2 + valor3 + valor4;
  var produto = valor1 * valor2 * valor3 * valor4;

  // Apresentando os resultados
  print('Resultados:');
  print('Soma dos valores: $soma');
  print('Produto dos valores: $produto');
}
